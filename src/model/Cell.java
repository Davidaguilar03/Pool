package model;

public class Cell {
	
	int x;
	int y;
	int value;
	char cel;
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public char getCel() {
		return cel;
	}
	public void setCel(char cel) {
		this.cel = cel;
	}
	
	
}
